<div> </div>
<div>周りの大人女子の先輩方からは、26歳...これからもっともっと楽しくなるじゃん！とよく言って頂きます♡笑</div>
<div> </div>
<div> </div>
<div>なんだか年々私も心が軽くなってきて！</div>
<div>ストレスフリーと言いますか、多分１番は自分との向き合い方が上手になってきたんだろうけど、個人的には26歳はもっとマイペースに、自分を大切に、感謝の気持ちを忘れずにやっていけたらいいなぁと思います。</div>
